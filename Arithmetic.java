package ArithmeticPack;
public class Arithmetic
{
    public void calc(int m,int n)
    {
        System.out.println(m+" + "+n+" = "+(m+n));
        System.out.println(m+" - "+n+" = "+(m-n));
        System.out.println(m+" * "+n+" = "+(m*n));
        System.out.println(m+" / "+n+" = "+(m/n));
        System.out.println(m+" % "+n+" = "+(m%n));

    }
}
